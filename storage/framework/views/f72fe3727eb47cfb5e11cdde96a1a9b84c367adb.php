<script>
    $(document).ready(function () {
        $('.cart_btn').each(function () {
            var $cartContainer = $(this);
            var $addToCartBtn = $cartContainer.find('.add-to-cart-btn');
            var $qtyBox = $cartContainer.find('.qtyBox');
            var $qtyInput = $qtyBox.find('.input-qty');

            $addToCartBtn.on('click', function () {
                $qtyInput.val(1);
                $qtyBox.show();
                $addToCartBtn.hide();
            });

            $qtyBox.find('.qty-btn-minus').on('click', function () {
                var currentValue = parseInt($qtyInput.val());
                if (currentValue > 0) {
                    $qtyInput.val(currentValue - 1);
                    if (currentValue - 1 === 0) {
                        $addToCartBtn.show();
                        $qtyBox.hide();
                    }
                }
            });

            $qtyBox.find('.qty-btn-plus').on('click', function () {
                var currentValue = parseInt($qtyInput.val());
                $qtyInput.val(currentValue + 1);
            });
        });
    });
</script>

